package clasesPrincipales;

public class Habilidad {
	
	String apodo;
	String descripcion;
	
	public Habilidad() {
		
	}
	
	public Habilidad(String apodo,String descripcion) {
		this.apodo=apodo;
		this.descripcion=descripcion;
	}

	public String getApodo() {
		return apodo;
	}

	public String getDescripcion() {
		return descripcion;
	}
	
	

}
